fn current_branch(path: &str) -> String {
    let mut name: String = String::new();
    let r: Repository = open(path);
    let head = r.head().expect("msg");
    if head.is_branch() {
        name = head.shorthand().unwrap_or("detached HEAD").to_string();
    }
    name
}
